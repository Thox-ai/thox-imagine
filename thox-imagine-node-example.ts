/**
 * Express.js integration example
 */
function expressExample() {
  const code = `
import express from 'express';
import { ThoxImagineClient } from './thox-imagine-node-example';

const app = express();
const client = new ThoxImagineClient();

app.use(express.json());

// Generate image endpoint
app.post('/api/generate', async (req, res) => {
  try {
    const { prompt } = req.body;

    if (!prompt) {
      return res.status(400).json({ error: 'Prompt is required' });
    }

    const result = await client.generate(prompt);

    if (!result.success) {
      return res.status(500).json({ error: result.error });
    }

    res.json({
      success: true,
      imagePath: result.imagePath,
      prompt: result.prompt,
      watermark: '© 2026 Thox.ai LLC. All Rights Reserved.',
    });

  } catch (error) {
    res.status(500).json({ error: 'Internal server error' });
  }
});

// Batch generate endpoint
app.post('/api/batch-generate', async (req, res) => {
  try {
    const { prompts } = req.body;

    if (!Array.isArray(prompts) || prompts.length === 0) {
      return res.status(400).json({ error: 'Prompts array is required' });
    }

    const results = await client.batchGenerate(prompts);

    res.json({
      success: true,
      results,
      total: results.length,
      successful: results.filter(r => r.success).length,
    });

  } catch (error) {
    res.status(500).json({ error: 'Internal server error' });
  }
});

// Get model info endpoint
app.get('/api/model-info', async (req, res) => {
  const info = await client.getModelInfo();
  res.json(info);
});

app.listen(3000, () => {
  console.log('Thox Imagine API server running on port 3000');
});
  `;

  console.log('\n📦 Express.js Integration Example:');
  console.log('─'.repeat(60));
  console.log(code);
}

