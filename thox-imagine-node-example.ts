import { exec } from 'child_process';
import { promisify } from 'util';
import * as fs from 'fs/promises';
import * as path from 'path';

const execAsync = promisify(exec);

/**
 * Configuration for Thox Imagine model
 */
interface ThoxImagineConfig {
  modelName: string;
  outputDir: string;
  defaultTemperature: number;
  defaultTopP: number;
  watermark: string;
}

/**
 * Image generation options
 */
interface GenerateOptions {
  temperature?: number;
  topP?: number;
  outputPath?: string;
}

/**
 * Image generation result
 */
interface GenerationResult {
  success: boolean;
  imagePath?: string;
  error?: string;
  prompt: string;
  timestamp: Date;
}

/**
 * Thox Imagine API Client for Node.js
 */
class ThoxImagineClient {
  private config: ThoxImagineConfig = {
    modelName: 'Thox-ai/thox-imagine',
    outputDir: 'thox-imagine-outputs',
    defaultTemperature: 0.8,
    defaultTopP: 0.95,
    watermark: '© 2026 Thox.ai LLC. All Rights Reserved. Generated by Thox Imagine.',
  };

  constructor(config?: Partial<ThoxImagineConfig>) {
    if (config) {
      this.config = { ...this.config, ...config };
    }
    this.ensureOutputDir();
  }

  /**
   * Ensure output directory exists
   */
  private async ensureOutputDir(): Promise<void> {
    try {
      await fs.mkdir(this.config.outputDir, { recursive: true });
    } catch (error) {
      console.error('Failed to create output directory:', error);
    }
  }

  /**
   * Verify that the Thox Imagine model is available
   */
  async verifyModel(): Promise<boolean> {
    try {
      const { stdout } = await execAsync('ollama list');

      if (!stdout.toLowerCase().includes(this.config.modelName.toLowerCase())) {
        throw new Error(
          `Model '${this.config.modelName}' not found. ` +
          'Please create it first with: ' +
          'ollama create Thox-ai/thox-imagine -f Modelfile.thox-imagine'
        );
      }

      return true;
    } catch (error) {
      throw new Error(`Failed to verify model: ${error}`);
    }
  }

  /**
   * Generate an image from a text prompt
   *
   * @param prompt - Text description of the desired image
   * @param options - Generation options
   * @returns Generation result with image path
   *
   * @example
   * ```typescript
   * const client = new ThoxImagineClient();
   * const result = await client.generate('A serene mountain landscape');
   * console.log('Image saved to:', result.imagePath);
   * ```
   */
  async generate(
    prompt: string,
    options: GenerateOptions = {}
  ): Promise<GenerationResult> {
    const startTime = new Date();

    try {
      if (!prompt || !prompt.trim()) {
        throw new Error('Prompt cannot be empty');
      }

      // Verify model availability
      await this.verifyModel();

      console.log('🎨 Generating image with Thox Imagine...');
      console.log('📝 Prompt:', prompt);

      // Generate output path
      const timestamp = new Date().toISOString().replace(/[:.]/g, '-');
      const outputPath = options.outputPath ||
        path.join(this.config.outputDir, `thox_imagine_${timestamp}.png`);

      // Run ollama generate command
      const command = `ollama run ${this.config.modelName} "${prompt}"`;

      await execAsync(command);

      console.log('✅ Image generated successfully!');
      console.log('🔒 Watermark:', this.config.watermark);
      console.log('💾 Saved to:', outputPath);

      return {
        success: true,
        imagePath: outputPath,
        prompt,
        timestamp: startTime,
      };

    } catch (error) {
      console.error('❌ Image generation failed:', error);

      return {
        success: false,
        error: error instanceof Error ? error.message : String(error),
        prompt,
        timestamp: startTime,
      };
    }
  }

  /**
   * Generate multiple images from an array of prompts
   *
   * @param prompts - Array of text prompts
   * @returns Array of generation results
   *
   * @example
   * ```typescript
   * const client = new ThoxImagineClient();
   * const prompts = [
   *   'A sunset over the ocean',
   *   'A futuristic city',
   *   'An ancient forest'
   * ];
   * const results = await client.batchGenerate(prompts);
   * console.log(`Generated ${results.length} images`);
   * ```
   */
  async batchGenerate(prompts: string[]): Promise<GenerationResult[]> {
    console.log(`🚀 Starting batch generation of ${prompts.length} images...`);

    const results: GenerationResult[] = [];

    for (let i = 0; i < prompts.length; i++) {
      console.log(`\n📊 Progress: ${i + 1}/${prompts.length}`);

      try {
        const result = await this.generate(prompts[i]);
        results.push(result);
      } catch (error) {
        console.error(`Failed to generate image ${i + 1}:`, error);
        results.push({
          success: false,
          error: error instanceof Error ? error.message : String(error),
          prompt: prompts[i],
          timestamp: new Date(),
        });
      }
    }

    const successful = results.filter(r => r.success).length;
    console.log(`\n✅ Batch complete! Generated ${successful}/${prompts.length} images`);

    return results;
  }

  /**
   * Get model information
   */
  async getModelInfo(): Promise<Record<string, any>> {
    try {
      const { stdout } = await execAsync('ollama list');

      const lines = stdout.split('\n');
      const modelLine = lines.find(line =>
        line.toLowerCase().includes(this.config.modelName.toLowerCase())
      );

      if (!modelLine) {
        return { error: 'Model not found' };
      }

      return {
        name: this.config.modelName,
        base: 'x/flux2-klein',
        parameters: '8B',
        quantization: 'FP4',
        watermark: this.config.watermark,
        uncensored: true,
        organization: 'Thox.ai LLC',
      };

    } catch (error) {
      return { error: `Failed to get model info: ${error}` };
    }
  }

  /**
   * List all generated images
   */
  async listGeneratedImages(): Promise<string[]> {
    try {
      const files = await fs.readdir(this.config.outputDir);

      const imageFiles = files
        .filter(file => file.endsWith('.png'))
        .map(file => path.join(this.config.outputDir, file));

      // Sort by modification time (newest first)
      const filesWithStats = await Promise.all(
        imageFiles.map(async (file) => ({
          file,
          mtime: (await fs.stat(file)).mtime,
        }))
      );

      return filesWithStats
        .sort((a, b) => b.mtime.getTime() - a.mtime.getTime())
        .map(item => item.file);

    } catch (error) {
      console.error('Failed to list images:', error);
      return [];
    }
  }
}

